#include <libft.h>

void	ft_putstrs_fd(char const *s, int fd)
{
	size_t i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
}
