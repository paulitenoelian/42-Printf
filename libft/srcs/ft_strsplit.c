#include "libft.h"

static int	word_count(const char *s, char c)
{
	int		i;
	int		count;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		if (s[i] != '\0' && s[i] != '\0')
			count++;
		while (s[i] != c && s[i] != '\0')
			i++;
	}
	return (count);
}

static int	word_len(const char *s, char c)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s[i] == c)
		i++;
	while (s[i] != '\0' && s[i] != c)
	{
		i++;
		len++;
	}
	return (len);
}

char		**ft_strsplit(char const *s, char c)
{
	int		i;
	int		j;
	int		k;
	char	**new;

	if (!s || !(new = (char **)malloc(sizeof(*new) *
					(word_count(s, c) + 1))))
		return (NULL);
	i = -1;
	j = 0;
	while (++i < word_count(s, c))
	{
		k = 0;
		if (!(new[i] = ft_strnew(word_len(&s[j], c) + 1)))
			new[i] = NULL;
		while (s[j] == c)
			j++;
		while (s[j] != c && s[j])
			new[i][k++] = s[j++];
		new[i][k] = '\0';
	}
	new[i] = 0;
	new[i] = NULL;
	return (new);
}
